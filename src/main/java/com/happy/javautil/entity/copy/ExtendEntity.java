package com.happy.javautil.entity.copy;

public class ExtendEntity extends BaseEntity {

    private Long extendId;

    private String extendName;

    private String extendCode;

    private Integer extendNumber;

    public Long getExtendId() {
        return extendId;
    }

    public void setExtendId(Long extendId) {
        this.extendId = extendId;
    }

    public String getExtendName() {
        return extendName;
    }

    public void setExtendName(String extendName) {
        this.extendName = extendName;
    }

    public String getExtendCode() {
        return extendCode;
    }

    public void setExtendCode(String extendCode) {
        this.extendCode = extendCode;
    }

    public Integer getExtendNumber() {
        return extendNumber;
    }

    public void setExtendNumber(Integer extendNumber) {
        this.extendNumber = extendNumber;
    }
}
